"""
Diverse calculation functions
"""


def add(num1, num2):
    """It returns the sum of two numbers"""
    return num1 + num2


def subtract(num1, num2):
    """It returns the subtraction of two numbers"""
    return num1 - num2


def multiply(num1, num2):
    """It returns the multiplication of two numbers"""
    return num1 * num2


def divide(num1, num2):
    """It returns the division of two numbers"""
    return num1 / num2


def print_operation(text: str):
    """It returns a string text containing the input"""
    return f'This is the input text: {text}'
